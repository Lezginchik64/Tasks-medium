# -- Позиционные и именованные аргументы --

# Позиционные аргументы - Все ранее написанные нами функции имели позиционные аргументы. Такие аргументы передаются без указания имен.
# Они называются позиционными, потому что именно по позиции, расположению аргумента, функция понимает, какому параметру он соответствует.
def diff(x, y):
    return x - y

res = diff(10, 3)    # используем позиционные аргументы
print(res)
# Такой код выведет число 7. При вызове функции diff() первому параметру x будет соответствовать первый переданный аргумент, 10, а второму параметру y  — второй аргумент, 3.


# -- Именованные аргументы --
# Аргументы, передаваемые с именами, называются именованными.
# При вызове функции можно использовать имена параметров из ее определения. Исключение составляют списки аргументов неопределенной длины, где используются аргументы со звездочкой, но об этом в следующем уроке.
# Все функции из предыдущих уроков можно вызывать, передавая им именованные аргументы.
def diff(x, y):
    return x - y

res = diff(x=10, y=3)   # используем именованные аргументы
print(res)
print()
# Использование именованных аргументов позволяет нарушать их позиционный порядок при вызове функции. Порядок упоминания именованных аргументов не имеет значения!
