num = [int(i) for i in input().split()]


def func(n):
    return sum([int(i) for i in str(n)]), n  # возвращает КОРТЕЖ (сумма_цифр, само_число)


num.sort(key=func)
print(*num)

# Ввод: 123 6 132 51 45 7 89
#
# Промежуточные кортежи:
# 123 → (1+2+3=6, 123) → (6, 123)
# 6   → (6, 6)         → (6, 6)
# 132 → (1+3+2=6, 132) → (6, 132)
# 51  → (5+1=6, 51)    → (6, 51)
# 45  → (4+5=9, 45)    → (9, 45)
# 7   → (7, 7)         → (7, 7)
# 89  → (8+9=17, 89)   → (17, 89)
#
# Сортировка:
# 1. По первому элементу: 6 < 7 < 9 < 17
# 2. При равных первых элементах (все где 6) сортируем по второму: 6 < 51 < 123 < 132
#
# Результат:
# 6 51 123 132 7 45 89
