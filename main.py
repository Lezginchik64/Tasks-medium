# -- Изменяемые типы в качестве значений по умолчанию --
# При использовании изменяемых типов данных в качестве значения параметра по умолчанию можно столкнуться с неожиданными результатами работы функции.
# Значение по умолчанию для параметра создается единожды при определении функции (обычно при загрузке модуля) и становится атрибутом (свойством) функции.
# Поэтому, если значение по умолчанию изменяемый объект, то его изменение повлияет на каждый следующий вызов функции.
# Чтобы посмотреть значения по умолчанию, можно использовать атрибут __defaults__.
def append(element, seq=[]):
    seq.append(element)
    return seq

print('Значение по умолчанию', append.__defaults__)
print(append(10))
print('Значение по умолчанию', append.__defaults__)
print(append(5))
print('Значение по умолчанию', append.__defaults__)
print(append(1))
print('Значение по умолчанию', append.__defaults__)
print()

# Для решения проблемы можно использовать константу None в качестве значения параметра по умолчанию, а в теле функции устанавливать нужное значение:
def append(element, seq=None):
    if seq is None:
        seq = []
    seq.append(element)
    return seq

print(append(10))
print(append(5))
print(append(1))


# Примечание 1. При написании функций стоит указывать более важные параметры первыми.

# Примечание 2. Параметр в программировании — принятый функцией аргумент.
# Термин «аргумент» подразумевает, что конкретно и какой конкретной функции было передано, а параметр — в каком качестве функция применила это принятое.
# То есть вызывающий код передает аргумент в параметр, который определен в описании (заголовке) функции.

def sum(param1, param2):    # param1 и param2 - это параметры
    return param1 + param2
sum(5, 6)   # 5 и 6 - это аргументы